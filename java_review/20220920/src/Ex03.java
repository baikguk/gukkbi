
public class Ex03 {
	
	public static void main(String[] args) {
		
		/*
		  
		  문자열 + any type -> 문자열
		  any type + 문자열 -> 문자열
		
	  */	
		 System.out.println(10 + 20);
		 
		 // 문자열에 + 를 사용하면 연결연산자라고 함
		 System.out.println("10" + "20");
		
		 // 10 + 20 = 30
		 System.out.println("10 + 20 = " + 30);
		 
		 // 10 + 20 = 1020
		 System.out.println("10 + 20 = " + 10 + 20);
		 
     // 10 + 20 = 30
		 System.out.println("10 + 20 = " + (10 + 20));
		 
		 System.out.println("" + 7 + 7);
		 
		 System.out.println(7 + 7 + "" + 7);
	}

}





